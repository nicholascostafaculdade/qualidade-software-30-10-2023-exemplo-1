package calculadora;

public class Calculadora {
	public int somaInteiros(int num1, int num2) {
		return num1 + num2;
	}
	
	public int subtraiInteiros(int num1, int num2) {
		return num1 - num2;
	}
	
	public int multiplicaInteiros(int num1, int num2) {
		return num1 * num2;
	}
	
	public int dividiInteiros(int num1, int num2) {
		return num1 / num2;
	}
}
